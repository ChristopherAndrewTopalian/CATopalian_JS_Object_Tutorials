// object_002.js

let pokemon = {
    pikachu: 'electric',
    charazar: 'fire'
};

for (let key in pokemon)
{
    // key shows pikachu name
    // pokemon[key] shows electric
    console.log(key + ' and ' + pokemon[key]);
}

/*
pikachu and electric
charazar and fire
*/

